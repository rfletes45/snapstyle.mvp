/**
 * Achievement lookup by ID
 */
export const ACHIEVEMENT_BY_ID: Record<string, GameAchievementDefinition> =
  ALL_GAME_ACHIEVEMENTS.reduce(
    (acc, achievement) => {
      acc[achievement.id] = achievement;
      return acc;
    },
    {} as Record<string, GameAchievementDefinition>,
  );

/**
 * Get achievements by category
 */
export function getAchievementsByCategory(
  category: AchievementCategory,
): GameAchievementDefinition[] {
  return ALL_GAME_ACHIEVEMENTS.filter((a) => a.category === category);
}

/**
 * Get total achievement count
 */
export function getTotalGameAchievementCount(): number {
  return ALL_GAME_ACHIEVEMENTS.length;
}

/**
 * Get visible achievements (non-secret or earned)
 */
export function getVisibleAchievements(
  earnedIds: Set<string>,
): GameAchievementDefinition[] {
  return ALL_GAME_ACHIEVEMENTS.filter((a) => !a.secret || earnedIds.has(a.id));
}

/**
 * Get achievement by ID
 */
export function getGameAchievementById(
  id: string,
): GameAchievementDefinition | undefined {
  return ACHIEVEMENT_BY_ID[id];
}

/**
 * Calculate completion stats
 */
export function getAchievementStats(earnedIds: Set<string>): {
  total: number;
  earned: number;
  percentage: number;
  byTier: Record<string, { total: number; earned: number }>;
} {
  const byTier: Record<string, { total: number; earned: number }> = {};

  for (const achievement of ALL_GAME_ACHIEVEMENTS) {
    if (!byTier[achievement.tier]) {
      byTier[achievement.tier] = { total: 0, earned: 0 };
    }
    byTier[achievement.tier].total++;
    if (earnedIds.has(achievement.id)) {
      byTier[achievement.tier].earned++;
    }
  }

  const total = ALL_GAME_ACHIEVEMENTS.length;
  const earned = earnedIds.size;

  return {
    total,
    earned,
    percentage: Math.round((earned / total) * 100),
    byTier,
  };
}
